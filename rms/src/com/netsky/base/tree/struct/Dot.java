package com.netsky.base.tree.struct;

/**
 * @author Chiang
 * 
 * 点显示类
 */
public class Dot {

	/**
	 * x坐标
	 */
	private String x;

	/**
	 * y坐标
	 */
	private String y;

	/**
	 * 颜色
	 */
	private String color;

	/**
	 * @return the x
	 */
	public String getX() {
		return x;
	}

	/**
	 * @param x
	 *            the x to set
	 */
	public void setX(String x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public String getY() {
		return y;
	}

	/**
	 * @param y
	 *            the y to set
	 */
	public void setY(String y) {
		this.y = y;
	}

	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}

	/**
	 * @param color
	 *            the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}

}
