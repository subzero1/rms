package com.netsky.base.tree.struct;

/**
 * @author Chiang
 * 
 * 路径名称显示类
 */
public class LineWord {

	private String x;

	private String y;

	private String htmlClass;

	private String name;

	/**
	 * @return the x
	 */
	public String getX() {
		return x;
	}

	/**
	 * @param x
	 *            the x to set
	 */
	public void setX(String x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public String getY() {
		return y;
	}

	/**
	 * @param y
	 *            the y to set
	 */
	public void setY(String y) {
		this.y = y;
	}

	/**
	 * @return the htmlClass
	 */
	public String getHtmlClass() {
		return htmlClass;
	}

	/**
	 * @param htmlClass
	 *            the htmlClass to set
	 */
	public void setHtmlClass(String htmlClass) {
		this.htmlClass = htmlClass;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
